def Add(addend1, addend2):
    return addend1 + addend2

def Substract(num1, num2):
    return num1 - num2

def Multiply(num1, num2):
    return num1 * num2

def Divide(num1, num2):
    return num1 / num2

sum = Add(5, 35)
difference = Substract(100, 55)
product = Multiply(5, 5)
divisor = Divide(100, 25)

print(sum)
print(difference)
print(product)
print(divisor)